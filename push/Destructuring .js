// Destructuring is a syntax in JavaScript that allows you to unpack values from arrays or properties from objects into distinct variables. It makes code cleaner and more readable by simplifying the extraction of multiple values.

const array = [1,3,4,6,7];
const [a,b,c,d,e] = array;
console.log(a,b,c,d,e); // Output: 1 3 4 6


const name = [1,2,4,5];
const[f,t,y,u] = name;
console.log(f,t,y,u); // Output: 1 2 4 5
